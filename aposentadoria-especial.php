  </p>
  <p align="justify" style="text-indent:10mm;">
    No que se refere ao serviço público, a o artigo 7º, da Instrução Normativa MPS/SPPS nº 1, de 22 de julho de 2010, dispõe que o procedimento de reconhecimento do tempo especial deverá ser instruído com os seguintes documentos:
  </p>
  <ol style="list-style: upper-roman;">
    <li>
        Laudo Técnico de Condições Ambientais do Trabalho – LTCAT, expedido por médico do trabalho ou engenheiro de segurança do trabalho que integre, de preferência, o quadro funcional da Administração Pública responsável pelo levantamento ambiental, podendo esse encargo ser atribuído a terceiro que comprove requisito de habilitação técnica.
    </li>
    <li>
     Formulário de informações sobre atividades exercidas em condições especiais – Perfil Profissiográfico Previdenciário – PPP.
    </li>
    <li>
      Desse modo, o setor de Recursos Humanos da Administração Pública deve manter os documentos necessários ao reconhecimento do tempo especial (LTCAT e PPP), bem como fornecê-lo ao servidor quando este o requerer para o processo de aposentadoria. 
    </li>
