        <li>Cópia do RG e CPF do(a) aposentado (a) falecido(a);</li>
        <li>Cópia do RG e CPF do dependente requerente, se tiver mais de um dependente é obrigatório cópias de todos os requerentes;</li>
        <li>No caso de filho maior inválido, comprovante de incapacidade física ou mental, ou seja, laudo médico pericial ou interdição judicial e termo de curatela;</li>
        <li>Cópia do comprovante de endereço atualizado, últimos 3 meses;</li>
        <li>Cópia da sentença de divórcio, separação judicial ou em cartório que comprove receber pensão alimentícia, para ex-cônjuge ou ex-companheiro;</li>
        <li>Declaração de não acúmulo de pensão por morte;</li>
        <li>No caso do requerente enteado (a) do (a) segurado (a) deve declarar que não é credor de alimentos em relação ao pai biológico;</li>
        <li>Cópia da certidão de casamento atualizada com averbação do óbito ou declaração de união estável firmada em conjunto (segurado e convivente) perante tabelião, no caso do convivente não ter a declaração de união estável em cartório e deverá apresentar no mínimo três provas documentais que comprove a convivência com o segurado;</li>
        <li>O RBPREV poderá solicitar outros documentos que achar pertinentes para instrução do processo de pensão.</li>

    </ol>

    
    <h4 class="badge badge-success mt-5 mb-3" style="background:red;">Atenção!!!! Todas as cópias deverão vir acompanhadas dos documentos originais para conferência ou autenticadas em cartório.</h4>  
   

     <p align="justify">
