    </div>
  </div>
</div>
<div class="container">
    <p align="justify" style="text-indent:10mm;">
       É o benefício concedido ao segurado que, por doença grave, moléstia profissional ou acidente, for considerado pela Junta Médica do Município, incapacitado de forma permanente para exercer qualquer atividade laboral.
    </p>

    <p align="justify" style="text-indent:10mm;">
         Nos termos do artigo 21 da Lei Municipal nº. 1.793, de 2009, alterada pela Lei Complementar nº 91, de 2020, são consideradas doenças graves, contagiosas ou incuráveis:
    </p>  

   
        <ul style="list-style: disc;text-indent:20mm;">
            <li>Tuberculose ativa; </li>
            <li>Alienação mental;</li>
            <li>Esclerose múltipla;</li>
            <li>Neoplasia maligna;</li>
            <li>Cegueira posterior ao ingresso no serviço público;</li>
            <li>Hanseníase;</li>
            <li>Cardiopatia grave;</li>
            <li>Doença de Parkinson;</li>
            <li>Paralisia irreversível e incapacitante;</li>
            <li>Espondiloartrose anquilosante;</li>
            <li>Nefropatia grave; </li>
            <li>Estados avançados do mal de Paget (osteíte deformante); </li>
            <li>Síndrome de imunodeficiência adquirida - AIDS.</li>
            <li>Hepatopatia grave;</li>
            <li>Fibrose cística (mucoviscidose)</li>
            <li>Outras que Lei municipal indicar com base na medicina especializada.</li>
        </ul>

    <p align="justify" class="py-4" style="text-indent:10mm;">
        As aposentadorias por invalidez serão precedidas de licença para tratamento de saúde por período não excedente a 36 meses, após esse prazo para o tratamento o servidor será readaptado para uma outra função ou será aposentado.
    </p>

    <h4 class="py-3">Valor do Benefício da aposentadoria por invalidez:</h4>

    <i class="fa fa-check py-2"></i>&nbsp;O valor do benefício da aposentadoria por invalidez enquadra-se em duas situações distintas, a saber:

    <p align="justify" style="text-indent:10mm;">
        Para os servidores públicos municipais que ingressaram no cargo efetivo até 31/12/2003, e que possuíam ou possuem a expectativa de se aposentar voluntariamente com proventos correspondentes à remuneração do cargo efetivo, se acometido de enfermidade ou evento que os incapacite para o exercício do cargo, com as doenças mencionadas anteriormente, terá seus proventos de aposentadoria calculados com base na remuneração do cargo efetivo. <b style="color:red;">(Há paridade em relação aos ativos)</b>
    </p>

    <p align="justify" style="text-indent:10mm;">
         Entretanto, para os servidores públicos municipais que ingressaram no cargo depois de 1º/01/2004, ou que ainda venham a ocupar cargo público, a forma de cálculo dos proventos é diferenciada, pois será realizado uma média das maiores remunerações que serviram de base para a contribuição previdenciária ao Regime Próprio ou aos demais regimes previdenciários, inclusive o Regime Geral, correspondentes a 80% de todo o período contributivo, apurado a partir de julho de 1994, atualizados. O resultado do cálculo não poderá exceder a remuneração do servidor no cargo efetivo em que se deu a aposentadoria nem poderá ser inferior a 70% dos proventos quando proporcional ao tempo de contribuição. <b style="color:red;">(Não há paridade em relação aos ativos)</b>
    </p>

      <h4 class="py-3">Procedimento da aposentadoria por invalidez:</h4>

      <p align="justify" style="text-indent:10mm;">
          O processo deverá estar instruído, obrigatoriamente, com o ofício de encaminhamento, a requisição e o laudo médico expedido pela Junta Médica a Prefeitura;
      </p>

      <p align="justify" style="text-indent:10mm;">
           Quando a junta médica encaminha o laudo, a Diretoria de Previdência do Instituto de Previdência do Município de Rio Branco, RBPREV, convoca o servidor para receber instruções sobre a regra da aposentadoria e cálculos dos proventos, momento em que preenche formulário e declarações e entrega documentos pessoais. 
       </p>

       <p align="justify" style="text-indent:10mm;">
            O RBPREV abrirá o processo e encaminhará ao setor de Recursos Humanos (Seção de Vida Funcional do Servidor) da Secretaria Municipal de Gestão Administrativa e Tecnologia da Informação - SEGATI, para elaborar histórico da carreira do servidor e juntar documentos e pasta funcional. 
       </p>

       <p align="justify" style="text-indent:10mm;">
           Somente após manifestação da Secretaria e devolução do processo com a pasta funcional, o RBPREV emitirá o ato concessório de aposentadoria.
       </p>
       <h4 class="py-3">Qual a Fundamentação legal:</h4>
       <p align="justify" style="text-indent:10mm;">
           A aposentadoria por invalidez é respaldada conforme artigo 20, I, da Lei Municipal nº 1.793, de 23 de dezembro de 2009.
       </p>

       <h4>Quais os documentos necessários para instrução do processo?</h4>


        <ul class=" py-2" style="list-style: disc;text-indent:20mm;">
            <li>Cópia do RG e do CPF;</li>
            <li>Cópia do PIS/PASEP;</li>
            <li>Certidão de casamento com averbação, se for o caso;</li>
            <li>Cópia da CTPS ou contrato de trabalho, se tiver;</li>
            <li>Cópia do registro de nascimento ou RG dos filhos, menores de 21 anos ou inválido;</li>
            <li>Cópia do RG, CPF e título de eleitor do cônjuge ou companheiro;</li>
            <li>Título de Eleitor do servidor;</li>
            <li>Comprovante de endereço atualizado e número de telefone para contato;</li>
            <li>Declaração de bens; (modelo no site)</li>
            <li>Declaração de não acumulação de cargos; (modelo no site)</li>
            <li>Declaração de dependentes; (modelo no site)</li>
            <li>Certidão de Tempo de Contribuição emitida pelo INSS;</li>
            <li>Certidão de Tempo de Contribuição de outros RPPS, se for o caso;</li>
            <li>Certificado de especialização, mestrado ou doutorado que deu direito ao recebimento do Adicional de Titulação ao servidor;</li>
            <li>Diploma de ensino fundamental, médio e superior, conforme exija o cargo que está exercendo.</li>
        </ul>
        <b style="color:red;">Todas as cópias deverão vir acompanhadas dos documentos originais para conferência ou autenticadas em cartório.</b><br/>
