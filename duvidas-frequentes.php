                  <div class="accordion-item">
                    <div class="accordion-heading">
                      <h3>
                        Que diz a Lei Federal 11.301/2006 sobre aposentadoria dos professores? 
                      </h3>
                      <div class="icon">
                        <i class="lnr lnr-chevron-right"></i>
                      </div>
                    </div>
                    <div class="accordion-content">
                      <p>A Lei Federal 11.301/2006 vem esclarecer o termo “funções de magistério”. Assim, consideram-se as funções de magistério os professores que exercem as funções de direção, coordenação e assessoramento, quando exercidas em estabelecimento de educação básica, e assim, equiparar essas funções àquelas exercidas pelos professores que atuam exclusivamente atividade em sala de aula, de modo a possibilitar a concessão de aposentadoria especial, com idade e tempo reduzido.</p>
                      <p align="justify" style="text-indent:10mm;">
                        Todavia, em razão da ausência de definições da própria Lei Federal, faz-se necessário a sua regulamentação por parte dos órgãos competentes, no sentido de esclarecer a sua retroatividade e quais são as funções nos estabelecimentos de ensino que se caracterizam como de assessoramento e de coordenação.
                      </p>
