  <h3 class="yellow f14">Abono de Permanência</h3>
  <p align="justify" style="text-indent:10mm;">
  O abono de permanência é devido ao servidor que tenha completado os 
requisitos para aposentadoria e opte por permanecer em atividade até 
completar 75 anos de idade. Tem como principal objetivo incentivar o servidor 
que já preenche os requisitos de aposentaria a permanecer na ativa, pelo 
menos até a compulsória. 
  </p>
  <p align="justify" style="text-indent:10mm;">
O servidor continua a contribuir mensalmente com o Regime de Previdência e 
o valor da contribuição descontado para o Fundo Previdenciário é devolvido    
pelo Município de Rio Branco, no caso o Tesouro Municipal. 
  </p>
  <p align="justify" style="text-indent:10mm;">
  O servidor deverá preencher requisitos para aposentadoria voluntária, 
conforme enquadramento abaixo: 
  </p>
  <ol type="a">
    <li>
      1º caso: prevista no art. 40, § 19 da Constituição Federal, ao servidor que, 
após 31/12/2003, data da publicação da Emenda Constitucional nº 41, de 2003, 
implemente todos os requisitos para aposentar-se voluntariamente com 
proventos integrais e mesmo assim decida permanecer em atividade, fará “jus” 
ao abono de permanência, pelo menos até completar 75 anos de idade; 
    </li>
    <li>
      2º caso: prevista no art. 2º, § 5º da Emenda Constitucional nº 41, de 2003, ao 
servidor que ingressou em cargo efetivo até 16/12/1998, data da publicação da 
Emenda Constitucional nº 20, de 1998, e implementou os requisitos elencados 
no caput do art. 2º da EC nº 41/2003, terá direito a perceber o abono de 
permanência, desde que permaneça na ativa, pelo menos até que implemente 
a idade de 75 anos de idade;
    </li>
    <li>
      3º caso: prevista no art. 3º, § 1º da Emenda Constitucional nº 41, de 2003, a 
todos os servidores que tenham cumprido os requisitos para a aposentadoria 
voluntária até 31/12/2003, data da publicação da Emenda Constitucional nº 
41/2003, o que corresponde a aposentar-se sob a égide do texto original da 
Constituição Federal de 1988, ou do texto emendado pela EC nº 20/1988 e que 
contém, ainda, com 30 anos de contribuição, se homem, ou 25 anos de 
contribuição, se mulher, desde que permaneçam trabalhando e, pelo menos, 
até o implemento da idade de 75 anos de idade.
    </li>
  </ol>
  <p align="justify" style="text-indent:10mm;">
    O servidor deverá expressamente fazer requerimento pela opção de 
permanência em atividade para receber o abono. Tal procedimento é feito na 
Seção da Vida Funcional do Servidor, na Secretária Municipal de Gestão 
Administrativa e Tecnologia da Informação – SEGATI. 
  </p>
  <p align="justify" style="text-indent:10mm;">
    A concessão do abono de permanecia é feito pela SEGATI, entretanto, o 
Instituto de Previdência do Município de Rio Branco (RBPREV), emite o 
relatório prévio dos requisitos da aposentadoria do servidor.
  </p>
