<div class="container" >
    <p align="justify" style="text-indent:10mm;">
        A pensão por morte é um benefício previdenciário pago pelo Instituto de Previdência do Município de Rio Branco – RBPREV aos dependentes do servidor público ativo ou aposentado que morreu ou que teve sua morte declarada pela justiça, como ocorre em casos de desaparecimento. 
    </p>

    <p align="justify" style="text-indent:10mm;">A Lei Municipal nº 1.793/2009 e suas alterações, estabelece aos dependentes dos segurados do Regime Próprio o direito a pensão por morte, que pode ser decorrente de morte de servidor em atividade e morte de aposentado.</p>
    
    <p align="justify" style="text-indent:10mm;">O art. 15 da Lei Municipal nº 1.793/2009, alterado pela Lei Complementar nº 91, de 3 de julho de 2020, menciona os dependentes do segurado:</p>

    <ul style="list-style: disc; text-indent:10mm;">
        <li>O cônjuge, o(a) companheiro(a), incluído nestes últimos, as uniões homoafetivas;</li>
        <li>Filho (a), menor de 21 anos; </li>
        <li>Filho (a), maior de 21 anos inválido (a);</li>
        <li>O pai e a mãe que comprovem dependência econômica do segurado; </li>
        <li>O irmão, não emancipado, de qualquer condição, menor de 21 (vinte um ano) anos que não possua bens ou rendimentos suficientes para o próprio sustento ou, se inválido, enquanto durar a invalidez. </li>
        <li>Enteados não beneficiários de outro regime previdenciário; </li>
        <li>Menor sob tutela; </li>
        <li>Cônjuge divorciado ou separado judicialmente com pensão alimentícia.</li>

    </ul>
    <br>
    <h5 style="color:red;">Observações importantes aos dependentes, no momento do requerimento da pensão por morte:<br/></h5>&nbsp;

  
    <p align="justify">
        Aos filhos, irmãos e enteados a comprovação da invalidez será feita mediante perícia realizada pela Junta Médica do Município, sendo que a invalidez deverá ter ocorrido antes de completar 21 anos de idade;
    </p>

   
    <p align="justify">
       Os pais do segurado para fazer jus a pensão por morte deverão comprovar junto ao RBPREV que dependiam economicamente do filho, bem como não existir em relação ao segurado falecido dependentes como: cônjuge ou companheiro e descendentes, ou seja, se o segurado tiver cônjuge e filhos, os pais estão excluídos da habilitação;
    </p>
    <p align="justify">
       O irmão do segurado, menor de 21 anos ou inválido, para fazer jus a pensão por morte deverá comprovar junto ao RBPREV a dependência econômica, bem como não existir em relação ao segurado falecido dependentes como: cônjuge ou companheiro e descendentes, ou seja, se o segurado tiver cônjuge e filhos, o irmão está excluído da habilitação.
    </p>
    
     <p align="justify">
       Os enteados podem ser habilitados a receber pensão previdenciária, desde que haja declaração escrita do segurado falecido, a comprovação da dependência econômica e que o enteado não seja beneficiário de outro regime previdenciário;
    </p>
    
     <p align="justify">
       O menor sob tutela é equiparado a filho do segurado, entretanto, para ser beneficiário de pensão por morte deve haver comprovação de que esse menor não tenha bens o suficiente para o próprio sustento e educação;
    </p>
    
     <p align="justify">
       O cônjuge divorciado ou separado judicialmente deverá comprovar junto ao RBPREV que recebia pensão alimentícia ou auxílio para sua subsistência, seja por sentença judicial, seja por ato de declaração pública feita em cartório pelo próprio segurado quando em vida;
    </p>
    
     <p align="justify">
       O cônjuge deve apresentar a certidão de casamento atualizada em cartório com averbações, inclusive de viúvo (a);
    </p>
    
     <p align="justify">
       O companheiro ou companheira deverá comprovar o vínculo da união estável e a dependência econômica, o qual deverá apresentar no momento do requerimento, no mínimo, três dos seguintes documentos:
    </p>

   
    <ul style="list-style: disc; text-indent:10mm;">
        <li>Certidão de nascimento de filho havido em comum;</li>
        <li>Certidão de casamento religioso ou Declaração de União Estável em cartório;</li>
        <li>Declaração do imposto de renda do segurado, em que conste o interessado como seu dependente;</li>
        <li>Declaração especial feita perante tabelião;</li>
        <li>Disposições testamentárias;</li>
        <li>Prova do mesmo domicílio;</li>
        <li>Prova de encargos domésticos evidentes e existência de sociedade ou comunhão nos atos da vida civil;</li>
        <li>Conta bancária em conjunta;</li>
        <li>Procuração ou fiança reciprocamente outorgada;</li>
        <li>Registro em associação de qualquer natureza, em que conste o interessado como dependente do segurado;</li>
        <li>Anotação constante de ficha ou livro de empregados;</li>
        <li>Apólice de seguro da qual conste o segurado como instituidor do seguro e a pessoa interessada como sua beneficiária;</li>
        <li>Ficha de tratamento em instituição de assistência médica, da qual conste o segurado como responsável;</li>
        <li>Escritura de compra e venda de imóvel pelo segurado em nome do dependente;</li>
        <li>Quaisquer outros que possam levar à convicção do fato a comprovar;</li>
       
    </ul>
    <br>
     <p align="justify">
        <span class="badge badge-success" style="background:red;">Importante!</span>&nbsp; Existindo mais de um pensionista a pensão será rateada entre todos em partes iguais.
    </p>

    <p  align="justify">
        <span class="badge badge-success" style="background:red;">Atenção</span>&nbsp;Para fins previdenciários, a existência de cônjuge, companheiro (a) e filhos como dependentes, exclui a possibilidade de concessão de benefícios para os pais; e a existência de pais como dependentes exclui a possibilidade de concessão de benefícios para os irmãos.
    </p>

       <h5 class="text-primary" align="justify" >
       Documentos Necessários para Instrução de Processo de Pensão por Morte de Servidor Ativo:
       </h5>
       <br>
       <ol style="list-style: lower-alpha; ">
       <li>Requerimento de pensão por morte de servidor em atividade;</li>
        <li>Cópia da certidão de óbito do(a) servidor (a) falecido (a);</li>
        <li>Cópia do RG e CPF do(a) servidor(a) falecido(a);</li>
        <li>Cópia do RG e CPF do dependente requerente, se tiver mais de um dependente é obrigatório cópias de todos os requerentes;</li>
        <li>No caso de filho maior inválido, será necessário comprovante de incapacidade física ou mental, ou seja, laudo médico pericial ou interdição judicial e termo de curatela;</li>
        <li>Cópia do comprovante de endereço atualizado, últimos 3 meses;</li>
        <li>Cópia da sentença de divórcio, separação judicial ou em cartório que comprove receber pensão alimentícia, para ex-cônjuge ou ex-companheiro;</li>
        <li>No caso do requerente enteado (a) do (a) segurado (a) deve declarar de que não é credor de alimentos em relação ao pai biológico;</li>
        <li>Cópia da certidão de casamento atualizada com averbação do óbito ou declaração de união estável firmada em conjunto (segurado e convivente) perante tabelião, no caso do convivente não ter a declaração de união estável em cartório e deverá apresentar no mínimo três provas que comprovem a convivência com o segurado;</li>
        <li>Declaração de não acúmulo de pensão por morte;</li>
        <li>O RBPREV poderá solicitar outros documentos que achar pertinentes para instrução do processo de pensão.</li>
       </ol>
       
       <h5 class="text-primary mt-5 mb-3" align="justify" >
        Documentos Necessários para Instrução de Processo de Pensão por Morte de aposentado
       </h5>


     <ol style="list-style: lower-alpha; ">
        <li>Requerimento da pensão por morte de aposentado;</li>
