  <h4 align="center" style="text-decoration: underline;">Histórico - CAPS</h4>
  <br>
  <h4>Conselho de Administração de Previdência Social – CAPS</h4>
</br>
<p align="justify" style="text-indent:10mm;">
  O CAPS foi instituído no âmbito da Previdência Municipal com a Lei nº 1.793, de 23 de dezembro de 2009, que criou o Regime Próprio de Previdência Social aos servidores públicos municipais de Rio Branco, como órgão colegiado de deliberação e supervisão do Regime Próprio, vinculado à estrutura administrativa do então Departamento de Previdência da Secretaria Municipal de Administração – SEAD.
</p>
<p align="justify" style="text-indent:10mm;">
  O Conselho Administrativo de Previdência Social, como foi denominado inicialmente, está em funcionamento desde abril de 2010, e é a forma institucional pela qual a Unidade Gestora do Regime Próprio desenvolve o diálogo com os representantes dos servidores públicos municipais.
</p>
<p align="justify" style="text-indent:10mm;">
  Com a Lei Municipal nº 1.963, de 20 de fevereiro de 2013, o RBPREV deixou de ser Departamento para tornar-se Autarquia, fazendo, assim, parte da Administração Pública Indireta do Município de Rio Branco, cuja fiscalização e deliberação da gestão do sistema previdenciário estão sob a responsabilidade dos Conselhos fiscal e de administração respectivamente, os quais tiveram seus conselheiros remanejados para compor a nova estrutura criada pela lei acima citada, até o final do mandato em curso.
</p>
<p align="justify" style="text-indent:10mm;">
  A finalidade do Conselho de Administração é deliberar sobre a política de Previdência Social e sobre a gestão do sistema previdenciário do Município de Rio Branco e exercerá suas competências legais nos termos do Regimento Interno do CAPS, link abaixo, que estabelece as normas de sua organização e funcionamento.
</p>
<p align="justify" style="text-indent:10mm;">
  Para o exercício de suas funções, o Conselho de Administração contará com o apoio da Diretoria Executiva do RBPREV.
</p>
<h4>Composição do CAPS</h4>
<br>
<p align="justify" style="text-indent:10mm;">A composição do conselho é constituído de 8 (oito) membros para um mandato de 2 (dois) anos, permitida um única recondução, assim distribuídos:</p>
<ul>
  <li>04 (quatro) representantes do Governo Municipal, e seus respectivos suplentes, indicados entre servidores efetivos do Poder Executivo e Legislativo, sendo 03 (três) servidores indicados pelo Chefe do Poder Executivo Municipal e 01 (um) pelo representante do Poder Legislativo;</li>
  <br />
  <li>04 (quatro) representantes dos segurados e beneficiários do Regime Próprio de Previdência Social, e seus respectivos suplentes, sendo 03 (três) representantes dos segurados em atividade e 01 (um) representante dos aposentados, indicados pelo Sindicato dos Servidores Municipais.</li>
</ul>
<br>
<h4>Competências do CAPS</h4>
<br>
<ol type="I">
  <li>examinar e aprovar a proposta orçamentária anual, bem como suas respectivas alterações propostas pela Diretoria Executiva;</li>
  <li>deliberar sobre a política de investimentos dos recursos administrados pelo RBPREV, determinando as práticas, princípios, mecanismos de controle e atuação na gestão de recursos e da administração da carteira de investimentos do RBPREV, por proposta da Diretoria Executiva;</li>
  <li>examinar e aprovar as avaliações atuariais e auditorias contábeis encaminhadas pela Diretoria Executiva;</li>
  <li>sugerir diretrizes e regras relativas à aplicação dos recursos econômicos-financeiros do RPPS, à política de benefícios e à adequação entre os planos de custeios e de benefícios;</li>
  <li>propor medidas tendentes ao contínuo aperfeiçoamento e modernização do sistema previdenciário;</li>
  <li>autorizar a contratação de agentes financeiros, bem como a celebração de contratos, convênios e ajustes pelo RBPREV para a gestão do FFIN e do FPREV;</li>
  <li>elaborar, aprovar e atualizar o Regimento Interno sempre que necessário, para adequação às normas vigentes;</li>
  <li>examinar e aprovar as contas do exercício e os seus demonstrativos contábeis, fiscais e administrativos, após parecer prévio do Conselho Fiscal;</li>
  <li>autorizar previamente o recebimento de bens e valores a título de dação em pagamento, observada a legislação vigente;</li>
  <li>autorizar negociação de eventuais valores e contribuições em atraso devido pelo Município de Rio Branco, observada a legislação vigente quanto ao parcelamento;</li>
  <li>aprovar resolução para constituir reserva técnica com finalidade específica de desenvolver o RBPREV, nos termos do parágrafo único do art. 6º, desta Lei;</li>
  <li>aprovar o Plano de Contas do RBPREV, juntamente com o Conselho Fiscal;</li>
  <li>autorizar projetos de lei para recomposição do equilíbrio financeiro e atuarial do regime e para alteração da política previdenciária do Município;</li>
  <li>desempenhar outras atividades correlatas e compatíveis com a função.</li>
</ol>
<br>
<h4>Base Legal</h4>
<br>
<ul>
  <li>Art. 1º, VI, da Lei Federal nº 9.717, de 27 de novembro de 1998.</li>
  <li>Art. 18 e 19, da Lei Municipal nº 1.963, de 20 de fevereiro de 2013.</li>
</ul>
