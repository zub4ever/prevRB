 <h4 align="center">Isenção de imposto de renda para aposentados e pensionistas 
portadores de doenças graves, contagiosas ou incuráveis.</h4>
 <p align="justify" style="text-indent:10mm;">
   O segurado do Regime Próprio de Previdência do Município de Rio Branco 
que tem doença incapacitante especificada na Lei Municipal nº 1.793, de 2009 c/c com a Lei Federal nº 7.713, de 1988 é isento do Imposto sobre a Renda da
Pessoa Física (IRPF), desde que se enquadrem cumulativamente as seguintes 
situações:
 </p>   


 <ol type="1">
  <li>1. Os rendimentos sejam relativos à aposentadoria ou pensão; e</li>
  <li>2. Seja portador de uma das seguintes doenças:</li>
  <ol type="a">
    <li>a. AIDS (Síndrome da Imunodeficiência Adquirida)</li>
    <li>b. Alienação mental</li>
    <li>c. Cardiopatia grave</li>
    <li>d. Cegueira posterior ao ingresso no serviço público</li>
    <li>e. Doença de Paget em estados avançados (Osteíte deformante)</li>
    <li>f. Doença de Parkinson</li>
    <li>g. Esclerose múltipla</li>
    <li>h. Espondiloartrose anquilosante</li>
    <li>i. Hanseníase</li>
    <li>j. Nefropatia grave</li>
    <li>k. Neoplasia maligna</li>
    <li>l. Paralisia irreversível e incapacitante</li>
    <li>m. Tuberculose ativa</li>
    <li>n. Hepatopatia grave</li>
    <li>o. Fibrose cística</li>
  </ol>
</ol>

<p align="justify" style="text-indent:10mm;">
Quando o servidor se aposenta por invalidez decorrente de doença
especificada no rol acima, o procedimento de isenção do IR é automático no 
lançamento da folha de pagamento, entretanto, quando adquirir a enfermidade
após o benefício da aposentadoria ou de pensão, o beneficiário deverá procurar
o Instituto de Previdência para agendar o serviço médico da Junta do Município
para que seja emitido laudo pericial para atestar o estado de doença ao 
enquadramento legal. 
</p> 

<p align="justify" style="text-indent:10mm;">
  O segurado ao dar entrada no processo de isenção para se submeter a 
avaliação pericial na Junta Médica do Município deverá trazer ao RBPREV:
</p> 

<ol type="a">
  <li>a. Documento de Identidade;</li>
  <li>b. Laudo do profissional assistente (médico ou odontólogo) atualizado
informando a doença, prevista em legislação, que acomete o aposentado
ou pensionista;</li>
  <li>c. Exames especializados que tenham sido feitos pelo aposentado ou 
pensionista e que possam contribuir para a junta médica oficial 
estabelecer a data do início da doença, a gravidade, o prognóstico, entre
outras informações importantes para o laudo pericial;</li>
  <li>d. Por fim, preencher o Formulário emitido pela Diretoria Previdenciária do 
RBPREV para encaminhamento da Junta Médica. </li>
</ol>

<b>Importante!!</b>

<p align="justify" style="text-indent:10mm;">
  A isenção de imposto de renda incide exclusivamente sobre proventos de
aposentadoria ou pensão, não sendo contemplados os servidores em atividade. 
</p>    

