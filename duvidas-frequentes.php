                    <div class="accordion-content">
                      <p>Não. A Emenda Constitucional 41/2003 extinguiu a aposentadoria proporcional. O termo proporcional é atualmente utilizado nas aposentadorias compulsórias (aos 70 anos), e por idade, de forma que o cálculo dos proventos serão proporcionais ao tempo de contribuição.
                      </p>
                    </div>
                  </div>
                  <div class="accordion-item">
                    <div class="accordion-heading">
                      <h3>
                        Há aposentadoria especial no serviço público vinculado ao RPPS?
                      </h3>
                      <div class="icon">
                        <i class="lnr lnr-chevron-right"></i>
                      </div>
                    </div>
                    <div class="accordion-content">
                      <p>Atualmente a Constituição Federal só garante a aposentadoria especial no Regime Próprio de Previdência para categoria do magistério. A Constituição prevê aposentadoria especial para outras categorias profissionais mediante critérios definidos em Lei Complementar.
                      </p>
                      <p align="justify" style="text-indent:10mm;">
                        Entretanto, com a edição da súmula vinculante nº 33, do Supremo Tribunal Federal o servidor público que se enquadre no artigo 40, parágrafo 4º, inciso III, da Constituição Federal poderá ser aposentado com aposentadoria especial aplicando, no que couber, as regras do Regime Geral de Previdência Social.
                      </p>
                    </div>
                  </div>
                  <div class="accordion-item">
                    <div class="accordion-heading">
                      <h3>
                        Quais são as exigências para o professor obter a aposentadoria especial?
                      </h3>
                      <div class="icon">
                        <i class="lnr lnr-chevron-right"></i>
                      </div>
                    </div>
                    <div class="accordion-content">
                      <p>O profissional do magistério, ou seja, o professor ou professora fará jus à aposentadoria especial quando preenche, cumulativamente, os seguintes requisitos:</p>
                      <ul>
                        <li>10 anos de efetivo exercício no serviço público; </li>
                        <li>5 anos no cargo de professor em sala de aula;</li>
                        <li>Tempo de contribuição de 25 anos para a mulher e 30 anos para homem; </li>
                        <li>Idade de 50 anos para mulher e 55 para homem no exercício do cargo em que se dará a aposentadoria;</li>
                        <li>Exercer exclusivamente o tempo de efetivo exercício nas funções de magistério na educação infantil, no ensino fundamental e/ou médio. </li>
                      </ul>
                    </div>
