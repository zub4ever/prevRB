    <h4>Auxílio-Reclusão</h4>

    <p align="justify" style="text-indent:10mm;">
        O auxílio reclusão é pago aos dependentes do segurado ativo recolhido à prisão, 
        o qual deixará de receber a remuneração por não estar em atividade. 
        O auxilio-reclusão deve ser mantido enquanto o servidor permanecer preso, 
        detento ou recluso, exceto na hipótese de sentença condenatória com trânsito 
        em julgado que resulte a perda do cargo.
    </p>

    <b>Valor do Benefício:</b>

    <p align="justify" style="text-indent:10mm;">
        O valor do benefício será de cem por cento da remuneração do servidor que perceba 
        até o valor máximo para este benefício, definido pelo RGPS, e que não receba, 
        de qualquer outra forma, remuneração dos cofres públicos do Município.
    </p>

    <p align="justify" style="text-indent:10mm;">
        O Benefício será pago a contar da data em que for requerido pelos dependentes do segurado.  
    </p>

    <p align="justify" style="text-indent:10mm;">
        O auxílio reclusão será dividido em partes iguais entre os dependentes do servidor.
    </p>

     <p align="justify" style="text-indent:10mm;">
       É importante ressaltar que o dependente terá que apresentar declaração bimestral, 
       comprovando que o servidor continua inserido no sistema penitenciário.
    </p>

    <b style="text-indent:10mm;">Tem direito ao benefício:</b>

    <ol type="a">
        <li>esposa ou companheira;</li>
        <li>filhos, enteados e tutelados até 18 anos ou inválido;</li>
    </ol>

    <b>Documentos Necessários:</b><br/><br/>

    <b>Cônjuge</b><br/><br/>

    <ul>
        <li>Requerimento inicial;</li>
        <li>Cópia do RG e CPF;</li>
        <li>Comprovante de Endereço atualizado;</li>
        <li>Extrato de conta corrente;</li>
        <li>Certidão de casamento</li>
        <li>Certidão emitida pela autoridade competente sobre o efetivo recolhimento do segurado à prisão 
        	e o respectivo regime de cumprimento da pena, sendo tal documento renovado e apresentado bimestralmente, 
        	sob pena de suspensão do benefício.
        </li>
    </ul>

    <b>Filho menor de 18 anos ou se maior inválido</b><br/><br/>

    <ul>
        <li>Requerimento inicial;</li>
        <li>Cópia do RG e CPF;</li>
        <li>Comprovante de Endereço atualizado;</li>
        <li>Extrato de conta corrente em nome do beneficiário;</li>
        <li>Laudo da junta médica no caso do maior invalido;</li>
        <li>Certidão emitida pela autoridade competente sobre o efetivo recolhimento do segurado à prisão e o respectivo 
        	regime de cumprimento da pena, sendo tal documento renovado e apresentado bimestralmente, sob pena de suspensão 
        	do benefício.
        </li>
    </ul>

    <b Style="color:red;">
        OBS: o pagamento do auxílio-reclusão cessa a partir do dia em que o servidor segurado for posto em liberdade, 
        ainda que condicional, ou do trânsito em julgado de sentença condenatória de que resulte a perda do cargo.
    </b>

