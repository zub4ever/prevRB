    <b>Como é realizado o cálculo dos proventos de aposentadoria? </b>
    <p align="justify" style="text-indent:10mm;">
         O valor do benefício será a média das remunerações que serviram de base para a contribuição previdenciária ao Regime 
         Próprio ou aos demais regimes previdenciários, inclusive o Regime Geral, correspondentes a 80% de todo o período contributivo, 
         apurado a partir de julho de 1994, atualizados. Todavia, esse valor não poderá exceder a remuneração do servidor no cargo 
         efetivo em que se deu a aposentadoria, nem poderá ser inferior ao salário mínimo.
    </p>

    <p align="justify" style="text-indent:10mm;">
       O segurado receberá o valor da média das remunerações, a título de proventos, proporcional ao tempo de contribuição.  
       <b style="color:red;">(Regra Permanente)</b>
    </p>

    <b>Qual o procedimento e a fundamentação legal da aposentadoria por idade?</b> 

    <p align="justify" style="text-indent:10mm;">
        A requerimento do servidor, o processo de aposentadoria por idade iniciará no setor de Recursos Humanos 
        (Seção de Vida Funcional do Servidor) da Secretaria Municipal de Administração e Gestão de Pessoas - SEAD.
    </p>

    <p align="justify" style="text-indent:10mm;">
         A fundamentação legal é o artigo 40, § 1º, inciso III, alínea b, da Constituição Federal e artigo 20, III, 
         alínea “b”, da Lei Municipal nº 1.793, de 23 de dezembro de 2009.
    </p>

    <b>Quais os documentos necessários para instrução do processo? </b>

    <p><i class="fa fa-check"></i>&nbsp; Quanto aos documentos pessoais do servidor:</p>

