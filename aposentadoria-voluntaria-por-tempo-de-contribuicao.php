	</p>

	<b>Qual o procedimento e a fundamentação legal da aposentadoria por tempo de contribuição e idade?</b>
	<p align="justify" style="text-indent:10mm;">
		O processo de aposentadoria é iniciado na Diretoria de Previdência do Instituto de Previdência do Município de Rio Branco, RBPREV, por meio de requerimento do servidor ou servidora que receberá instruções sobre a regra da aposentadoria e cálculos dos proventos, momento em que preenche requerimento e declarações e entrega de documentos. 
		<p>
		O RBPREV abrirá o processo e encaminhará ao setor de Recursos Humanos (Seção de Vida Funcional do Servidor) da Secretaria Municipal de Gestão Administrativa e Tecnologia da Informação - SEGATI, para elaborar histórico da carreira do servidor e juntar documentos e pasta funcional. </p>
		<p>
		Somente após manifestação da Secretaria e devolução do processo com a pasta funcional, o RBPREV emitirá o ato concessório de aposentadoria.</p>
	</p>

