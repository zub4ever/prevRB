<p align="justify" style="text-indent:10mm;">É benefício previdenciário concedido, obrigatoriamente, ao segurado por haver alcançado o limite máximo de idade permitido no serviço público. Assim, ao completar 75 (setenta e cinco anos) de idade o segurado terá sua aposentadoria concedida independentemente de requerimento.</p>

<h5>Como é realizado o cálculo dos proventos de aposentadoria?</h5>

<p align="justify" style="text-indent:10mm;">O valor do benefício será a média das remunerações que serviram de base para a contribuição previdenciária ao Regime Próprio ou aos demais regimes previdenciários, inclusive o Regime Geral, correspondentes a 80% de todo o período contributivo, apurado a partir de julho de 1994, atualizados. Todavia, esse valor não poderá exceder a remuneração do servidor no cargo efetivo em que se deu a aposentadoria nem poderá ser inferior ao salário mínimo.
O segurado receberá o valor da média das remunerações a título de proventos, proporcional ao tempo de contribuição.</p>

<h5>Qual o procedimento aposentadoria compulsória?</h5>

<p align="justify" style="text-indent:10mm;">O processo de aposentadoria é iniciado na Diretoria de Previdência do Instituto de Previdência do Município de Rio Branco, RBPREV, ex-ofício, que convocará o servidor ou servidora para comparecer ao Instituto para receber instruções sobre a regra da aposentadoria e cálculos dos proventos, momento em que preenche formulários, entrega documentos pessoais para instrução do processo de aposentadoria.</p>

<p align="justify" style="text-indent:10mm;">O RBPREV abrirá o processo e encaminhará ao setor de Recursos Humanos (Seção de Vida Funcional do Servidor) da Secretaria Municipal de Gestão Administrativa e Tecnologia da Informação - SEGATI, para elaborar histórico da carreira do servidor e juntar documentos e pasta funcional. 
Somente após manifestação da Secretaria e devolução do processo com a pasta funcional, o RBPREV emitirá o ato concessório de aposentadoria.</p>

<h5>Qual a fundamentação Jurídica da aposentadoria Compulsória?</h5>
<p align="justify" style="text-indent:10mm;">
 	<li>Ementa Constitucional nº 88, de 7 de maio de 2015, que alterou o art. 40, §1º, inciso II da Constituição Federal.</li>
 	<li>Lei Complementar Federal nº 152, de 3 dezembro de 2015.</li>
	<li>Lei Municipal nº. 1.793, de 2009, alterada pela Lei Complementar nº 91, de 3 de junho de 2020, art. 20, inciso II. </li>
</p>
