    <h4 align="center" style="text-decoration: underline;">Histórico - CONFIS</h4>

    <h4 class="yellow f14">Conselho Fiscal - CONFIS</h4></br>

    <p align="justify" style="text-indent:10mm;">
        O Conselho Fiscal foi instituído no âmbito da Previdência Municipal com a Lei nº 1.793, de 23 de dezembro de 2009, que criou o Regime Próprio de Previdência Social aos servidores públicos municipais de Rio Branco, como órgão colegiado de fiscalização do Regime Próprio, vinculado à estrutura administrativa do então Departamento de Previdência da Secretaria Municipal de Administração – SEAD.
    </p>

    <p align="justify" style="text-indent:10mm;">
        O Conselho Fiscal está em funcionamento desde abril de 2010, e é a forma institucional pela qual o referido Conselho atua como órgão de fiscalização e de controle interno da gestão da Unidade Gestora do Regime Próprio, desenvolvendo o diálogo com os servidores públicos municipais.
    </p>    
    
    <p align="justify" style="text-indent:10mm;"> 
        Com a Lei Municipal nº 1.963, de 20 de fevereiro de 2013, o RBPREV deixou de ser Departamento para tornar-se Autarquia, fazendo, assim, parte da Administração Pública Indireta do Município de Rio Branco, cuja fiscalização e deliberação da gestão do sistema previdenciário estão sob a responsabilidade dos Conselhos fiscal e de administração respectivamente, os quais tiveram seus conselheiros remanejados para compor a nova estrutura criada pela lei acima citada, até o final do mandato em curso.
    </p>

    <p align="justify" style="text-indent:10mm;">
        A finalidade do Conselho Fiscal é fiscalizar e fazer o papel de controle interno da gestão do sistema previdenciário do Município de Rio Branco e exercerá suas competências legais nos termos do Regimento Interno do CONFIS, link abaixo, que estabelece as normas de sua organização e funcionamento.
    </p>

    <p align="justify" style="text-indent:10mm;">
        Para o exercício de suas funções, o Conselho Fiscal contará com o apoio da Diretoria Executiva do RBPREV.
    </p>

    <h4>Composição do CONFIS</h4>

    <p align="justify" style="text-indent:10mm;">A composição do conselho é constituído de 03 (três) membros titulares e seus respectivos suplentes, para um mandato de 2 (dois) anos, permitida um única recondução, assim distribuídos:</p>

    <ul>
        <li>01 (um) membro e respectivo suplente, indicados pelo Chefe do Poder Executivo Municipal dentre os servidores ativos;</li><br />
        <li>02 (dois) membros e respectivos suplentes indicados pelo Sindicato dos Servidores Municipais dentre os segurados do RBPREV.</li>
    </ul>
    <br>
    <h4>Competências do CONFIS</h4>

    <ol type="I">
        <li>examinar os demonstrativos contábeis e financeiros e emitir parecer das contas apresentadas e encaminhá-lo ao Conselho de Administração;</li>
        <li>acompanhar o recolhimento mensal das contribuições em face do prazo estabelecido na Lei Municipal nº 1.793, de 23 de dezembro de 2009, sendo que na ocorrência de eventuais irregularidades, deve notificar a Diretoria Executiva e o Conselho de Administração para adoção das medidas cabíveis;</li>
        <li>examinar os procedimentos relativos aos benefícios previdenciários concedidos aos segurados e dependentes;</li>
        <li>pronunciar-se sobre a alienação de bens patrimoniais do RBPREV;</li>
        <li>denunciar às autoridades municipais e às associações sindicais dos servidores, assim como ao Tribunal de Contas do Estado e ao Ministério Público, fatos ou ocorrências comprovadamente desabonadoras, havidas na gestão contábil, patrimonial, financeira ou operacional dos Fundos;</li>
        <li>examinar e dar parecer prévio nos contratos, acordos, convênios e processos licitatórios celebrados;</li>
        <li>encaminhar ao Conselho de Administração, anualmente, dentro dos prazos legais, juntamente com o seu parecer técnico, o relatório da Diretoria Executiva, relativo ao exercício anterior, o processo de tomada de contas, o balanço anual e o investimento a ele referente, assim como o relatório estatístico dos benefícios prestados;</li>
        <li>fiscalizar a execução da política de aplicação das receitas e despesas do RBPREV;</li>
        <li>desempenhar outras atividades correlatas e compatíveis com a função.</li>
        
    </ol>
    <br>
    <h4>Base Legal</h4>
    <ul>
        <li>Art. 1º, VI, da Lei Federal nº 9.717, de 27 de novembro de 1998.</li>
        <li>Art. 20 e 21, da Lei Municipal nº 1.963, de 20 de fevereiro de 2013.</li>
    </ul>
