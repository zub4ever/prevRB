    <h4 class="yellow f14" align="center">Histórico do Regime de Previdência do Município de Rio Branco</h4></br>

    <h4 class="yellow f14" align="auto">RBPREV 2009 - 2013</h4></br>

    <p align="justify" style="text-indent:10mm;">
       O Regime Próprio de Previdência Social do Município de Rio Branco foi criado
em 2009, com a Lei Municipal nº 1.793, de 23 dezembro de 2009, tendo vigência,
efetivamente, em 1º de abril de 2010, garantindo aos servidores efetivos e seus
dependentes, benefícios previdenciários instituídos em lei. Até esse período,
todos os servidores públicos do Município eram empregados públicos, regidos
pela CLT, e vinculados Regime Geral – RGPS, ocasião em que mudou o regime
previdenciário e o regime de trabalho para o estatutário, este último com a Lei
Municipal nº 1.794, de 30 de dezembro de 2009.
   </p>

   <p align="justify" style="text-indent:10mm;">
    A partir da decisão de instituir um novo regime de trabalho e o RPPS, o
Governo Municipal de Rio Branco designou uma comissão composta por
técnicos do município, consultores especializados, sindicatos e vereadores, que
realizaram uma análise detalhada da sustentabilidade, solvência e menor custo
previdenciário do regime próprio em relação ao Regime Geral de Previdência
Social - RGPS. Esse trabalho foi subsidiado, dentre vários elementos, pelo estudo
atuarial realizado após o recadastramento dos servidores efetivos do município.
</p>    

<p align="justify" style="text-indent:10mm;">
    De modo a garantir um sistema previdenciário equilibrado e com previsão
de auto-sustentabilidade, a Lei Municipal 1.793/2009, criou o Fundo
Previdenciário - FPREV e Fundo Financeiro - FFIN, aderindo ao sistema de
segregação de massa previdenciária com contas e contabilização separadas. A
fonte de custeio do RPPS com alíquotas normais, sendo a patronal de 17,41%
(acima da média nacional) e de 11% (onze por cento) para os segurados. Além
disso, o FPREV receberia um aporte financeiro de R$ 200.000,00 mensais pelo
período de 5 anos, e o Munícipio, arcaria com as despesas administrativas pelo
período de 3 anos – de modo que o custo que seria revestindo para taxa de
administração no percentual de 2%, ficaria no próprio fundo previdenciário.
</p>

<p align="justify" style="text-indent:10mm;">
    O Rio Branco Previdência - RBPREV foi criado como um Departamento,
órgão integrante da estrutura da Secretaria Municipal de Administração,
assegurando ao servidor público os benefícios de aposentadoria e pensão por
morte previstos no art. 40 da Constituição Federal, sendo de filiação obrigatória,
segundo regra geral, e possuindo caráter contributivo, pois, funciona como um
seguro para utilização nas situações de risco social. O Departamento foi extinto
quando se criou a Autarquia com a Lei Municipal nº 1.963, de 20 de fevereiro de
2013.

</p>

<h4 class="yellow f14" align="auto">RBPREV 2013 a 2020</h4></br>

<p align="justify" style="text-indent:10mm;">
    O Instituto de Previdência do Município de Rio Branco – RBPREV foi criado como Autarquia pela Lei Municipal nº 1.963, de 20 de fevereiro de 2013, integrante da administração indireta da Prefeitura de Rio Branco. É dotada de personalidade jurídica de direito público e de autonomia administrativa, contábil, financeira, orçamentária e patrimonial, com a finalidade de gerir o Regime Próprio de Previdência Social dos servidores públicos do Município de Rio Branco (ACRE). Vincula-se, para fins de controle finalístico, à Secretaria Municipal de Gestão Administrativa e Tecnologia da Informação - SEGATI.  Nesse mesmo ano, o estudo atuarial apresentou déficit atuarial e o Município aprovou lei para pagamento de alíquotas suplementares para sanar o déficit. A partir de 2013, os conselhos de previdência, Conselho Fiscal e Conselho Administração, aprovaram constituição de reserva técnica financeira, com a sobra da taxa de administração, para construção da Sede Própria do RBPREV.
</p>

<p align="justify" style="text-indent:10mm;">
    Em 2019, o Município de Rio Branco doou uma área de terra de 6.727,33m² para a construção da sede do RBPREV, localizada na Avenida Ceará, Bairro Doca Furtado, ao lado do Mercado Francisco de Assis Marinheiro, bem como aprovou a Lei Complementar nº 72, de 05/11/19, que instituiu o Plano de Cargos, Carreira e Remuneração - PCCR dos Servidores do Instituto de Previdência do Município de Rio Branco para o quadro de pessoal permanente do Instituto.
</p>

<p align="justify" style="text-indent:10mm;">
    Em 2019, foi aprovado a estrutura organizacional do RBPREV, regido pelo Decreto Municipal nº 1.607, de 18 de outubro, ocasião em que foi aprovado o Decreto nº 1.606, da mesma data, que estabeleceu as atribuições dos órgãos integrantes da Autarquia.
</p>

<p align="justify" style="text-indent:10mm;">
    Em 2020, o Município alterou a alíquota do segurado para 14%, por meio da Lei Complementar nº. 91, de 3 de julho, em atendimento a Emenda Constitucional nº 103/2019. Em 2020 o RBPREV também recebeu a Certificação Institucional com a implantação do programa Pró-gestão que visa o reconhecimento das boas práticas de gestão previdenciária e iniciou as obras da sede do Instituto de Previdência. 
</p>
